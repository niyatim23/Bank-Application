    private void withdrawButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawButtonActionPerformed
        StringBuilder warnings = new StringBuilder();
        if(amountField.getText().isEmpty())
            warnings.append("Withdrawl amount is required");
        else{
            double amount = 0;
            try{
                amount = Double.parseDouble(amountField.getText());
                int result = JOptionPane.showConfirmDialog(this,"Withdraw Rs."+amount+" from the account?\nTransaction Fee :Rs."+customer.getAccount().getTransactionFee());
                if(result == JOptionPane.OK_OPTION){
                    try{
                        customer.getAccount().withdraw(amount);
                        this.dispose();
                    }
                    catch(InsufficientFundsException ex){
                        warnings.append("You do not have sufficient funds!");                   
                    }
                    
                }
            }
            catch(NumberFormatException ex){
                warnings.append("Initial Withdrawl must be a number!");
            }
        }
        if(warnings.length() > 0)
            JOptionPane.showMessageDialog(this,warnings.toString(),"Withdrawl Warnings",WARNING_MESSAGE);
    }//GEN-LAST:event_withdrawButtonActionPerformed


