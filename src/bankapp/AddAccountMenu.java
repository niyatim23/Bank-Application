    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        StringBuilder warnings = new StringBuilder();
        String firstName = "", lastName = "" ,ssn = "" ,depositString =  "";
        Double amount = 0.0;
        if(firstNameField.getText().isEmpty()){
            warnings.append("First Name must not be empty\n");
        }
        else{
            firstName = firstNameField.getText();
        }
        if(lastNameField.getText().isEmpty()){
            warnings.append("Last Name must not be empty\n");
        }
        else{
            lastName = lastNameField.getText();
        }
        if(!ssnField.getText().matches("(\\d{3}-?){2}\\d{4}+")){
           warnings.append("Unique Identity Number must be a 10-digit number of the format xxx-xxx-xxxx\n");
        }
        else{
            ssn = ssnField.getText().replace("-","");
        }
        if(initialDepositField.getText().isEmpty()){
            warnings.append("Initial Deposit must be entered");
        }
        else{
            try{
                amount = Double.parseDouble(initialDepositField.getText());
            }
            catch(NumberFormatException ex){
                warnings.append("Ãnitial Deposit must be a number");
            }
        }
        if(warnings.length() > 0)
            JOptionPane.showMessageDialog(this,warnings.toString(),"Input Warnings" ,JOptionPane.WARNING_MESSAGE);
        else
        {   Account account = null;
            if(accountTypeField.getSelectedItem().toString() == "Checking") //JOptionPane.showMessageDialog(this, firstName+"\n"+lastName+"\n"+ssn+"\n"+amount);
               if(amount >= 100){
                    account = new Checking(amount);
               }
               else{
                   warnings.append("Amount must be greater than Rs.100 for Checking Account");
               }
            else if(accountTypeField.getSelectedItem().toString() == "Savings")
                if(amount >= 50){
                    account = new Savings(amount);
               }
               else{
                   warnings.append("Amount must be greater than Rs.50 for Checking Account");
               }
            if(account != null){
                customer = new Customer(firstName, lastName, ssn, account);
                bank.addCustomer(customer);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this,warnings.toString(),"Input Warnings" ,JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_okButtonActionPerformed

  
